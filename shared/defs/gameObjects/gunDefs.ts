import { type DeepPartial, util } from "../../utils/util";
import type { Vec2 } from "../../utils/v2";

export interface GunDef {
    readonly type: "gun";
    name: string;
    quality?: number;
    aimAssist?:boolean;
    autoSwitch?:boolean;
    fireMode: "auto" | "single" | "burst" | "dual";
    secondFireMode?: "auto" | "single" | "burst" | "dual";
    caseTiming: "shoot" | "reload";
    ammo: string;
    secondAmmo?: string;
    ammoSpawnCount: number;
    maxClip: number;
    maxReload: number;
    extendedClip: number;
    extendedReload: number;
    reloadTime: number;
    backpackFed?: boolean;
    fireDelay: number;
    switchDelay: number;
    barrelLength: number;
    barrelOffset: number;
    recoilTime: number;
    moveSpread: number;
    shotSpread: number;
    bulletCount: number;
    bulletType: string;
    upgraded?: {
        gun: string;
        cost: number;
    };
    headshotMult: number;
    speed: {
        equip: number;
        attack: number;
    };
    lootImg: {
        sprite: string;
        tint: number;
        border: string;
        borderTint: number;
        scale: number;
        innerScale?: number;
        rot?: number;
        mirror?: boolean;
    };
    worldImg: {
        sprite: string;
        scale: Vec2;
        tint: number;
        leftHandOffset?: Vec2;
        recoil: number;
        gunOffset?: Vec2;
        handsBelow?: boolean;
        magImg?: {
            sprite: string;
            pos: Vec2;
            top?: boolean;
        };
    };
    particle: {
        shellScale: number;
        shellOffset: number;
        shellOffsetY?: number;
        shellForward?: number;
        shellReverse?: boolean;
    };
    sound: {
        shoot: string;
        reload: string;
        pickup: string;
        empty: string;
        deploy: string;
        cycle?: string;
        pull?: string;
        shootLast?: string;
        shootTeam?: Record<string, string>;
        shootAlt?: string;
        fallOff?: number;
        reloadAlt?: string;
    };
    pullDelay?: number;
    isDual?: boolean;
    pistol?: boolean;
    dualOffset?: number;
    ammoInfinite?: boolean;
    noPotatoSwap?: boolean;
    noDrop?: boolean;
    burstDelay?: number;
    burstCount?: number;
    dualWieldType?: string;
    outsideOnly?: boolean;
    ignoreEndlessAmmo?: boolean;
    noSplinter?: boolean;
    isLauncher?: boolean;
    deployGroup?: number;
    projType?: string;
    ignoreDetune?: boolean;
    aimDelay?: boolean;
    isBullpup?: boolean;
    jitter?: number;
    maxReloadAlt?: number;
    extendedReloadAlt?: number;
    reloadTimeAlt?: number;
    toMouseHit?: boolean;
    burstSounds?: number;
    perk?: string;
    modifiedSound?: string;
    bulletTypeExtra?: string;
    extraBulletTrigger?: number;
    bulletTypeMix?: Array<{
        bulletType: string;
        count: number;
    }>;
}

function defineGunSkin(baseType: string, params: DeepPartial<GunDef>): GunDef {
    return util.mergeDeep({}, BaseDefs[baseType], params);
}

